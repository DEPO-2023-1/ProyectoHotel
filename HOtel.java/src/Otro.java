public class Otro {
    

    //atributos//
    private String nombre;
    private double precio;

    //constructor//
    public Otro (String nombre, double precio){
        this.nombre = nombre;
        this.precio = precio;
    }
    public String getNombre() {
        return Nombre;
    }
    
    public void setNombre(String Nombre) {
        this.Nombre = Nombre;
    }
    
    public double getPrecio() {
        return precio;
    }
    
    public void setPrecio(double precio) {
        this.precio = precio;
    }
}
